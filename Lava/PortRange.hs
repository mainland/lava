module Lava.PortRange
where
import Lava.Netlist

-------------------------------------------------------------------------------

portRange :: Int -> Dir -> Int -> [Int]
portRange a To b = [a..b]
portRange a Downto b = [b..a]

-------------------------------------------------------------------------------

portOrder :: Int -> Dir -> Int -> [Int]
portOrder a To b = [a..b]
portOrder a Downto b = reverse [b..a]

